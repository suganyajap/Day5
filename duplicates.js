//Remove duplicates from an array
var arr=['a','b','c','a','d','d'];
var unique=function (a) {
    var seen = {};
    var out = [];
    var len = a.length;
    var j = 0;
    for(var i = 0; i < len; i++) {
         var item = a[i];
         if(seen[item] !== 1) {
               seen[item] = 1;
               out[j++] = item;
         }
    }
    return out;
};
//calling function
console.log(unique(arr));